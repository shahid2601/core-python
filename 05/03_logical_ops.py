x = True
y = False

# Logical AND
result_and = x and y
# Logical OR
result_or = x or y
# Logical NOT
result_not_x = not x
result_not_y = not y

# Output results
print("Logical AND (x and y): ", result_and)
print("Logical OR (x or y): ", result_or)
print("Logical NOT (not x): ", result_not_x)
print("Logical NOT (not y): ", result_not_y)